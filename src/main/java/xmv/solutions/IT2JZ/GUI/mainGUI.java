    private boolean testCreatePageOpened = false;
    private boolean importFileLoaded = false;

    private void updateActiveStatusForExportButton() {
        if (!testCreatePageOpened || !importFileLoaded) {
            buttonExport.setEnabled(false);
            labelExport.setText("");
        } else {
            buttonExport.setEnabled(true);
        }

    }

    private boolean importFileLoadable = false;

    private void updateActiveStatusForLoadButton() {
        buttonLoad.setEnabled(importFileLoadable);
        if(!importFileLoadable) {
            labelLoad.setText("");
        }
    }

