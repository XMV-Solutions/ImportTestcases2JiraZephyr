    private void buttonLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonLoadActionPerformed
        // Reset
        excel2TestcaseConverter = null;
        importFileLoaded = false;
        jProgressBar1.setValue(0);
        labelExport.setText("");
        updateActiveStatusForExportButton();

        excel2TestcaseConverter = new Excel2TestcaseConverter(
                inputSummary.getText(),
                inputDescription.getText(),
                inputStepName.getText(),
                inputStepData.getText(),
                inputStepExpectedResult.getText(),
                excelFile);

        labelLoad.setText("");
        if (excel2TestcaseConverter.parse() != null) {
            importFileLoaded = true;
            labelLoad.setText(java.util.ResourceBundle.getBundle("de_DE").getString("ERFOLGREICH $X TESTFÄLLE GELADEN!").
                    replace("$x", Integer.toString(
                            excel2TestcaseConverter.getTestcases().size()
                    )));
        }
        updateActiveStatusForExportButton();
    }//GEN-LAST:event_buttonLoadActionPerformed

    private void buttonSelectFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSelectFileActionPerformed

        Settings settings = Settings.getInstance();
        String startPath = settings.getProperty(
                Settings.Keys.ExcelFilePath.name()
        );

        if (startPath == null || startPath.isEmpty()) {
            excelFile = ExcelFile.openFileChooser();
        } else {
            excelFile = ExcelFile.openFileChooser(startPath);
        }

        importFileLoadable = (excelFile != null && excelFile.testExcelFile());
        updateActiveStatusForLoadButton();

        if (importFileLoadable) {
            settings.setProperty(
                    Settings.Keys.ExcelFilePath.name(),
                    new File(excelFile.getPath()).getParent());
            settings.store();
            
            labelExcelFile.setText(java.text.MessageFormat.format(java.util.ResourceBundle.getBundle("de_DE").getString("File kann geladen werden"), new Object[]{excelFile.getType().name()}));
        } else {
            labelExcelFile.setText(java.util.ResourceBundle.getBundle("de_DE").getString("FILE KANN NICHT GELADEN WERDEN. BITTE ANDERES WÄHLEN."));
        }
        
        updateActiveStatusForExportButton();
        updateActiveStatusForLoadButton();
    }//GEN-LAST:event_buttonSelectFileActionPerformed

