    private void buttonCheckTestCreatePageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCheckTestCreatePageActionPerformed

        if (jira == null || !browserWorks) {
            labelTestCreatePageResult.setText(java.util.ResourceBundle.getBundle("de_DE").getString("ZUERST BROWSER ÖFFNEN UND OFFEN LASSEN"));
            testCreatePageOpened = false;
        } else if (jira.testCreatePageOpened()) {
            labelTestCreatePageResult.setText(java.util.ResourceBundle.getBundle("de_DE").getString("<HTML>TEST CREATE PAGE ERKANNT! <FONT COLOR=GREEN>OK!</FONT></HTML>"));
            testCreatePageOpened = true;
        } else {
            labelTestCreatePageResult.setText(java.util.ResourceBundle.getBundle("de_DE").getString("TEST CREATE PAGE WURDE NICHT ERKANNT. BITTE SCHRITT 2 WIEDERHOLEN"));
            testCreatePageOpened = false;
        }

        updateActiveStatusForExportButton();
    }//GEN-LAST:event_buttonCheckTestCreatePageActionPerformed

    private void jButtonOpenBrowserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOpenBrowserActionPerformed

        Jira.Browser selectedBrowser = null;

        if (selectBrowser.getSelectedObjects().length >= 1) {
            selectedBrowser = (Jira.Browser) selectBrowser.getSelectedObjects()[0];
        }

        jira = new Jira(selectedBrowser);

        browserWorks = jira.testBrowserOpen();
    }//GEN-LAST:event_jButtonOpenBrowserActionPerformed

